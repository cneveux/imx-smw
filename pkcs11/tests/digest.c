static int digest_all_mechanisms(CK_FUNCTION_LIST_PTR pfunc, bool final)
{
	int status = TEST_FAIL;

	CK_RV ret = CKR_OK;
	CK_SESSION_HANDLE sess = 0;
	CK_MECHANISM digmech = { 0 };
	CK_BYTE_PTR message = NULL_PTR;
	CK_ULONG message_length = 0;
	CK_BYTE_PTR digest = NULL_PTR;
	CK_ULONG digest_length = 0;
	unsigned int i = 0;
	bool match = false;

	SUBTEST_START();

	if (util_open_rw_session(pfunc, 0, &sess) != TEST_PASS)
		goto end;

	for (; i < MECH_ID_NB; i++) {
		TEST_OUT("Check digest %s\n", DIGEST_NAME(i));
		digmech.mechanism = DIGEST_MECHANISM(i);
		ret = pfunc->C_DigestInit(sess, &digmech);
		if (CHECK_CK_RV(CKR_OK, "C_DigestInit"))
			goto end;

		digest_length = DIGEST_LENGTH(i);
		digest = malloc(digest_length);
		if (CHECK_EXPECTED(digest, "Allocation error"))
			goto end;

		if (final) {
			ret = pfunc->C_DigestFinal(sess, digest,
						   &digest_length);
			if (CHECK_CK_RV(CKR_OK, "C_DigestFinal"))
				goto end;

			match = check_digest(TV_DIGEST_ND(i), DIGEST_LENGTH(i),
					     digest, digest_length);
			if (CHECK_EXPECTED(match, "Digest mismatch"))
				goto end;
		} else {
			message = (CK_BYTE_PTR)test_vector[i].message;
			message_length = test_vector[i].message_length;
			ret = pfunc->C_Digest(sess, message, message_length,
					      digest, &digest_length);
			if (CHECK_CK_RV(CKR_OK, "C_Digest"))
				goto end;

			match = check_digest(TV_DIGEST(i), DIGEST_LENGTH(i),
					     digest, digest_length);
			if (CHECK_EXPECTED(match, "Digest mismatch"))
				goto end;
		}

		free(digest);
		digest = NULL_PTR;
	}

	status = TEST_PASS;
end:
	util_close_session(pfunc, &sess);

	if (digest)
		free(digest);

	SUBTEST_END(status);
	return status;
}

